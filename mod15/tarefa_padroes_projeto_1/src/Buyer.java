public class Buyer {

    private String ferrariCar;
    private String porscheCar;

    public Buyer(String ferrariCar, String porscheCar) {
        this.ferrariCar = ferrariCar;
        this.porscheCar = porscheCar;
    }

    public String ferrariCar() {
        return ferrariCar;
    }


    public String porscheCar() {
        return porscheCar;
    }
}
